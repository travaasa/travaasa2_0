        <h2 name="faq1">TRAVAASA WEDDING COMMITMENT</h2>

        <h4 id="isacontractrequiredtohaveaweddingattravaasa">Is a contract required to have a wedding at Travaasa?</h4>

        <ul>
            <li>All weddings require a contract. The contract is between Travaasa and the Responsible Party</li>
        </ul>

        <h4 id="whatistheguestroomrequirementtohavemyweddingattravaasa">What is the guest room requirement to have my wedding at Travaasa?</h4>
        <ul>
            <li>Travaasa Events: In order to book a wedding and secure event space at Travaasa a minimum of 8 rooms each night or 15 cumulative room nights is required as part of your contract. Please keep in mind that Travaasa does not allow minors under the age of 16 on the grounds.</li>
            <li>Property Buyout: An exclusive buyout of the property allows you and your guests’ exclusive use of the grounds. Site fees are still applicable. A buyout requires a signed contract for all 70-guest rooms with a two night minimum stay (must include the night of the event). Children are permitted during the hours of the buyout; beginning at 3 p.m. on the arrival date and ending at 12 p.m. on departure day. Minors are not allowed on property outside of those time periods. Buyouts also require a Food &amp; Beverage Minimum Revenue and a Spa Minimum Revenue.</li>
            <li>Farm Events: Weddings booked at the Travaasa Farm do not require guests rooms and minors are allowed at the Farm with appropriate adult supervision.</li>
        </ul>
        <h2 name="faq2">CEREMONY/ RECEPTION</h2>
        <h4 id="isthereanon-siteweddingcoordinator">Is there an on-site wedding coordinator?</h4>
        <ul>
            <li>As the selected venue for your wedding ceremony and/or reception Travaasa will provide expert assistance with food and beverage service and set up of all included items in the Travaasa Event Fee. Your event will be assigned a Venue Coordinator who will provide a detailed questionnaire to assist in your menu selections, set up requirements, proposed timeline, and vendor requirements. The Venue Coordinator will assist in ensuring that you have met all of your contractual obligations to the hotel and offer suggestions on how best to utilize the venue.</li>
            <li>Travaasa requires that you work with a professional wedding planner to assist in planning your ceremony &amp; reception, facilitation of the ceremony rehearsal, finding outside vendors for needs beyond the inclusions of the hotel, and to orchestrate coordination the day of your ceremony and reception.</li>
        </ul>
        <h4 id="whathappensifitrains">What happens if it rains?</h4>
        <ul>
            <li>A rain back-up plan should be decided upon during the booking phase of the wedding. Due to the make-up of the facility, not all venues within the property offer a rain backup at your selected site (i.e. The Farm). Any additional costs including tenting, outdoor flooring, umbrellas, heaters, etc. are at the discretion of The Wedding Couple and will incur additional costs. Travaasa requires a weather call a minumum of 6 hours prior to event start.</li>
        </ul>
        <h4 id="willibeallowedtohaveaceremonyrehearsal">Will I be allowed to have a ceremony rehearsal?</h4>
        <ul>
            <li>Yes! A date and time for your wedding rehearsal will be scheduled and typically takes place the day before the event.</li>
        </ul>
        <h4 id="whencanihaveaccesstostartsettingupmydcor">When can I have access to start setting up my décor?</h4>

        <ul>
            <li>Décor should be set-up the day of your event. A specific timeline of vendor arrivals should be submitted with your wedding planning questionnaire. Décor requiring additional set time (i.e. tenting- etc.) should be disclosed at least 2&#8211;3 months prior to your event date and is subject to availability and additional charges based on requirements.</li>
            <li>Please note that Travaasa does not handle set up of outside decor. All pick-ups must be made the night of the event unless alternative arrangements have been made in advance and approved by Travaasa. There is a $500 equipment handling fee per day for items not picked up as noted or if alternative date/time is not provided and/or approved in advance.</li>
        </ul>

        <h4 id="aretheiranyrestrictionsastohowicandecorate">Are their any restrictions as to how I can decorate?</h4>

        <ul>
            <li>You cannot staple, tack, nail or glue anything to any surface, structure or natural element (tree’s etc.). Please no confetti or glitter.</li>
        </ul>

