					<h2>Hana Town Center</h2>
					<p>Get a glimpse of community life by taking a stroll through the Town Center and browse the shops. The bank, post office, Hana Ranch Restaurant, florist, Hana Ranch Store and several gift shops are located here. Town Center is just a five-minute walk from the hotel.</p>
					<h2>Wai'anapanapa State Park</h2>
					<p>Walk along the ancient "King's Trail," explore a legendary freshwater cave, discover important archeological sites, visit Maui's only black-sand beach and of course, it all overlooks the Pacific Ocean.</p>
					<h2>Hana Bay</h2>
					<p><em>Kapueokahi</em> (the lone owl) is commonly referred to as "Hana Bay." It was the major shipping port for the sugar companies. Today, this quarter-mile slice of black and white sand is a favorite with local families for swimming, snorkeling and strolling. Snorkeling equipment is available for purchase in the gift shop.</p>
					<h2>Hana Cultural Center</h2>
					<p>Just a short walk from Travaasa Hana, the Cultural Center houses a fascinating collection of historical photos, documents and artifacts. The gift shop helps support the collections and any donation is greatly appreciated.</p>
					<p>Open Monday – Friday 10:00 a.m.–4:00 pm.</p>
					<h2>Kahanu Garden</h2>
					<p>The Garden is home to some of Hawai'i's most precious treasures. <em>Pi'ilanihale</em>, a <em>heiau</em> or temple, is believed to be the largest man-made, pre-Western structure in all of Polynesia. Self-guided tours are offered Monday–Friday 10:00 a.m.–2:00 p.m. by the National Tropical Botanical Gardens. Guided tours are available on Saturdays by appointment. Advance reservations are required.</p>
					<h2>‘Ōhe'o Gulch at Haleakala National Park</h2>
					<p>The Park features the famous "Pools of Ōheo" – a series of cascading pools leading to the ocean. The Waimoku Falls Trail is a five-mile round trip hike that takes you through a bamboo forest to the 400-foot Waimoku Falls. NOTE: The Park is approximately 11 miles from Hana town and we urge you to drive there with caution and heed all caution signs while at the park.</p>
